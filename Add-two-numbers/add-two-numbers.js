function addTwoNumbers(num1, num2) {
    return num1 + num2;
}

//Test
console.log(addTwoNumbers(5, 6));   //Expected output 11 
console.log(addTwoNumbers(10, 0));  //Expected output 10
console.log(addTwoNumbers(-5, -5)); //Expected output -10
console.log(addTwoNumbers(1, -9));  //Expected output -8
console.log(addTwoNumbers(-5, 10)); //Expected output 5